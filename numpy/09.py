import numpy as np

x2 = np.array([[12,  1,  3,  7],
       [ 4,  0,  2,  3],
       [ 0,  0,  6,  9]])

print(x2[:3, :2])     #[[12 1],[4, 0], [0, 0]]

print(x2[:3, ::2])   #[[12, 3], [4,  2] [0,  6]]
 
print(x2[::-1, ::-1])   #[7, 3, 1, 12], [3, 2, 0, 4] [9, 6, 0 ,0]
                     # [9, 6, 0 ,0][3, 2, 0, 4][7, 3, 1, 12]

